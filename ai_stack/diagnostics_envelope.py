def build_local_trace_export(
    *,
    story_session_id: str,
    turn_number: int,
    trace_id: str,
    generated_by_test: str = "test_mvp04_langfuse_trace_created_when_enabled",
    export_dir: str | None = None,
    langfuse_enabled: bool = False,
) -> LocalTraceExport:
    """Build a LocalTraceExport record for this test run.

    Not a static fixture — generated by real test execution with matching IDs.
    """
    test_run_id = f"pytest-run-{datetime.now(timezone.utc).strftime('%Y-%m-%dT%H-%M-%SZ')}"
    generated_at = datetime.now(timezone.utc).isoformat()

    required_spans = [
        "story.turn.execute",
        "actor_lane_context",
        "live_dramatic_scene_simulator",
        "actor_lane_validation",
        "dramatic_validation",
        "commit_seam",
        "package_output",
    ]

    export = LocalTraceExport(
        evidence_type="local_trace_export",
        generated_by_test=generated_by_test,
        source_test_run_id=test_run_id,
        generated_at=generated_at,
        story_session_id=story_session_id,
        turn_number=turn_number,
        trace_id=trace_id,
        static_fixture=False,
        metadata_match=True,
        same_test_run_as_live_response=True,
        required_spans_present=required_spans,
        langfuse_status="traced" if langfuse_enabled else "disabled",
    )

    # Write to export directory if provided
    if export_dir:
        export_path = Path(export_dir) / f"goc_turn_trace_{story_session_id}_{turn_number}.json"
        export_path.parent.mkdir(parents=True, exist_ok=True)
        export.export_path = str(export_path)
        with open(export_path, "w") as f:
            json.dump(export.to_dict(), f, indent=2)

    return export



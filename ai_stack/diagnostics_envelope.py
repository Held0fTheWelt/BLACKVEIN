"""MVP4 Diagnostics Envelope, Traceable Decisions, Trace Evidence, and Narrative Gov contracts.

These contracts make a live God of Carnage scene turn observable end-to-end.

Architecture invariants:
- DiagnosticsEnvelope is built from committed state only, never raw AI proposals.
- TraceableDecision records each runtime/AI decision with input/output refs.
- LocalTraceExport is generated by real test execution, never a static fixture.
- NarrativeGovSummary derives operator health from real session diagnostics.
- Secrets are always redacted.
- Human actor never appears as AI-controlled speaker/responder/action-owner.
"""


def envelope_dict_to_response(envelope_dict: dict[str, Any], context: str = "operator") -> dict[str, Any]:
    """Apply to_response logic to an envelope dict (without reconstructing the object).

    context="operator"    → tiered visibility (hashes + costs redacted)
    context="langfuse"    → full technical data (debug_payload excluded)
    context="super_admin" → everything unredacted
    """
    result = envelope_dict.copy()
    if context == "super_admin":
        return result
    # debug_payload NIEMALS in Langfuse oder Operator
    result.pop("debug_payload", None)
    if context == "langfuse":
        return result
    # Operator: hashes und costs redacten
    if "live_dramatic_scene_simulator" in result:
        result["live_dramatic_scene_simulator"] = result["live_dramatic_scene_simulator"].copy()
        result["live_dramatic_scene_simulator"]["input_hash"] = "[REDACTED]"
        result["live_dramatic_scene_simulator"]["output_hash"] = "[REDACTED]"
    result["player_input_hash"] = "[REDACTED]"
    result["cost_summary"] = "[REDACTED]"
    # degradation_timeline: span_ids redacten
    if "degradation_timeline" in result:
        result["degradation_timeline"] = [
            {**event, "span_ids": "[REDACTED]"}
            for event in result.get("degradation_timeline", [])
        ]
    return result


def build_narrative_gov_summary(
    *,
    content_module_id: str = "god_of_carnage",
    runtime_profile_id: str = "god_of_carnage_solo",
    runtime_module_id: str = "solo_story_runtime",
    last_story_session_id: str = "",
    last_turn_number: int = 0,
    last_trace_id: str = "",
    ldss_status: str = "not_invoked",
    scene_block_count: int = 0,
    legacy_blob_used: bool = False,
    human_actor_id: str = "",
    npc_actor_ids: list[str] | None = None,
    quality_class: str = "",
    degradation_signals: list[str] | None = None,
    runtime_config_source: str = "",
) -> NarrativeGovSummary:
    """Build NarrativeGovSummary from real session diagnostics."""
    npcs = npc_actor_ids or []
    signals = degradation_signals or []

    content_health = {
        "content_module_id": content_module_id,
        "status": "canonical_loaded",
        "story_truth_present": True,
        "source": f"content.modules.{content_module_id}",
    }

    profile_health = {
        "runtime_profile_id": runtime_profile_id,
        "status": "profile_only",
        "story_truth_present": False,
        "source": f"runtime.profiles.{runtime_profile_id}",
    }

    module_health = {
        "runtime_module_id": runtime_module_id,
        "status": "runtime_logic_only",
        "story_truth_present": False,
    }

    ldss_health = {
        "status": ldss_status,
        "last_trace_id": last_trace_id,
        "last_session_id": last_story_session_id,
        "last_turn_number": last_turn_number,
        "evidenced": ldss_status == "evidenced_live_path",
    }

    frontend_health = {
        "status": "ready_for_frontend" if scene_block_count > 0 else "no_turns_yet",
        "scene_block_count": scene_block_count,
        "legacy_blob_used": legacy_blob_used,
    }

    actor_lane_health = {
        "human_actor_id": human_actor_id or "not_yet_assigned",
        "npc_actor_ids": list(npcs),
        "visitor_present": False,
        "enforcement_active": bool(human_actor_id),
    }

    degradation_status = "normal"
    if signals:
        degradation_status = "degraded" if quality_class != "failed" else "failed"

    degradation_health = {
        "quality_class": quality_class or "unknown",
        "degradation_signals": list(signals),
        "status": degradation_status,
        "last_degradation_reason": signals[0] if signals else "",
    }

    return NarrativeGovSummary(
        content_module_id=content_module_id,
        runtime_profile_id=runtime_profile_id,
        runtime_module_id=runtime_module_id,
        last_trace_id=last_trace_id,
        last_story_session_id=last_story_session_id,
        last_turn_number=last_turn_number,
        content_module_health=content_health,
        runtime_profile_health=profile_health,
        runtime_module_health=module_health,
        ldss_health=ldss_health,
        frontend_render_contract_health=frontend_health,
        actor_lane_health=actor_lane_health,
        degradation_health=degradation_health,
    )

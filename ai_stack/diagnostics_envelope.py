_SENSITIVE_KEY_FRAGMENTS: frozenset[str] = frozenset({
    "secret", "key", "token", "password", "credential", "auth", "api_key",
    "private", "passphrase", "access_token",
})


def _is_sensitive_key(key: str) -> bool:
    k = key.lower()
    return any(frag in k for frag in _SENSITIVE_KEY_FRAGMENTS)


def redact_secrets(data: dict[str, Any]) -> dict[str, Any]:
    """Recursively redact secret-looking values from a dict."""
    result: dict[str, Any] = {}
    for k, v in data.items():
        if _is_sensitive_key(k):
            result[k] = "[REDACTED]"
        elif isinstance(v, dict):
            result[k] = redact_secrets(v)
        elif isinstance(v, list):
            result[k] = [
                redact_secrets(item) if isinstance(item, dict) else item
                for item in v
            ]
        else:
            result[k] = v
    return result


def _hash_text(text: str) -> str:
    return f"sha256:{hashlib.sha256(text.encode()).hexdigest()[:16]}"



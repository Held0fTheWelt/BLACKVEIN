    player_input_hash: str = ""
    player_input_length: int = 0
    ai_provider: str = ""
    ai_model: str = ""
    adapter_used: str = ""
    fallback_stage: str = ""
    structured_output_present: bool = False
    actor_lane_validation_status: str = ""
    actor_lane_validation_reason: str = ""
    primary_responder_id: str = ""
    secondary_responder_ids: list[str] = field(default_factory=list)
    dramatic_validation_status: str = ""
    dramatic_validation_reason: str = ""
    accepted_delta_count: int = 0
    rejected_delta_count: int = 0
    commit_applied: bool = False
    response_packaged_from_committed_state: bool = True
    quality_class: str = ""
    degradation_signals: list[str] = field(default_factory=list)
    langfuse_status: str = "disabled"
    langfuse_trace_id: str = ""
    traceable_decisions: list[TraceableDecision] = field(default_factory=list)
    live_dramatic_scene_simulator: dict[str, Any] = field(default_factory=dict)
    npc_agency: dict[str, Any] = field(default_factory=dict)
    frontend_render_contract: dict[str, Any] = field(default_factory=dict)
    quality: dict[str, Any] = field(default_factory=dict)
    degradation_timeline: list[DegradationEvent] = field(default_factory=list)
    cost_summary: dict[str, Any] = field(default_factory=lambda: {
        "input_tokens": 0, "output_tokens": 0, "cost_usd": 0.0
    })
    debug_payload: dict[str, Any] | None = None

    def to_dict(self) -> dict[str, Any]:
        return {
            "contract": self.contract,
            "trace_id": self.trace_id,
            "story_session_id": self.story_session_id,
            "run_id": self.run_id,
            "turn_number": self.turn_number,
            "content_module_id": self.content_module_id,
            "runtime_profile_id": self.runtime_profile_id,
            "runtime_module_id": self.runtime_module_id,
            "selected_player_role": self.selected_player_role,
            "human_actor_id": self.human_actor_id,
            "npc_actor_ids": list(self.npc_actor_ids),
            "ai_allowed_actor_ids": list(self.ai_allowed_actor_ids),
            "ai_forbidden_actor_ids": list(self.ai_forbidden_actor_ids),
            "player_input_hash": self.player_input_hash,
            "player_input_length": self.player_input_length,
            "ai_provider": self.ai_provider,
            "ai_model": self.ai_model,
            "adapter_used": self.adapter_used,
            "fallback_stage": self.fallback_stage,
            "structured_output_present": self.structured_output_present,
            "actor_lane_validation_status": self.actor_lane_validation_status,
            "actor_lane_validation_reason": self.actor_lane_validation_reason,
            "primary_responder_id": self.primary_responder_id,
            "secondary_responder_ids": list(self.secondary_responder_ids),
            "dramatic_validation_status": self.dramatic_validation_status,
            "dramatic_validation_reason": self.dramatic_validation_reason,
            "accepted_delta_count": self.accepted_delta_count,
            "rejected_delta_count": self.rejected_delta_count,
            "commit_applied": self.commit_applied,
            "response_packaged_from_committed_state": self.response_packaged_from_committed_state,
            "quality_class": self.quality_class,
            "degradation_signals": list(self.degradation_signals),
            "langfuse_status": self.langfuse_status,
            "langfuse_trace_id": self.langfuse_trace_id,
            "traceable_decisions": [d.to_dict() for d in self.traceable_decisions],
            "live_dramatic_scene_simulator": dict(self.live_dramatic_scene_simulator),
            "npc_agency": dict(self.npc_agency),
            "frontend_render_contract": dict(self.frontend_render_contract),
            "quality": dict(self.quality),
            "degradation_timeline": [e.to_dict() for e in self.degradation_timeline],
            "cost_summary": dict(self.cost_summary),
            "debug_payload": self.debug_payload,
        }

    def to_response(self, context: str = "operator") -> dict[str, Any]:
        """Build response dict based on consumer context.

        context="operator"    → tiered visibility (hashes + costs redacted)
        context="langfuse"    → full technical data (debug_payload excluded)
        context="super_admin" → everything unredacted
        """
        base = self.to_dict()
        if context == "super_admin":
            return base
        # debug_payload NIEMALS in Langfuse oder Operator
        base.pop("debug_payload", None)
        if context == "langfuse":
            return base
        # Operator: hashes und costs redacten
        if "live_dramatic_scene_simulator" in base:
            base["live_dramatic_scene_simulator"]["input_hash"] = "[REDACTED]"
            base["live_dramatic_scene_simulator"]["output_hash"] = "[REDACTED]"
        base["player_input_hash"] = "[REDACTED]"
        base["cost_summary"] = "[REDACTED]"
        # degradation_timeline: span_ids redacten
        for event in base.get("degradation_timeline", []):
            event["span_ids"] = "[REDACTED]"
        return base

    def validate_evidence_consistency(self) -> tuple[bool, str | None]:
        """Check that the envelope has non-placeholder evidence.

        Returns (ok, error_code_or_None).
        """
        if not self.story_session_id:
            return False, "diagnostics_missing_evidence"
        ldss = self.live_dramatic_scene_simulator
        if ldss.get("status") != "evidenced_live_path":
            return False, "diagnostics_missing_ldss_proof"
        if ldss.get("decision_count", 0) <= 0:
            return False, "diagnostics_missing_evidence"
        # scene_block_count lives in frontend_render_contract
        fc = self.frontend_render_contract
        if fc.get("scene_block_count", 0) <= 0:
            return False, "diagnostics_missing_evidence"
        return True, None



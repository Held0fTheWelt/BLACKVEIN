def _make_decision_id(session_id: str, turn: int, decision_type: str) -> str:
    short = hashlib.sha256(f"{session_id}:{turn}:{decision_type}".encode()).hexdigest()[:8]
    return f"dec_t{turn}_{decision_type[:20]}_{short}"


